package source.dto;

public class Entry {
	
	//fields
	private String rank;
	private String title;
	private String numberComments;
	private String numberPoints;
	
	//getters and setters
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getNumberComments() {
		return numberComments;
	}
	public void setNumberComments(String numberComments) {
		this.numberComments = numberComments;
	}
	public String getNumberPoints() {
		return numberPoints;
	}
	public void setNumberPoints(String numberPoints) {
		this.numberPoints = numberPoints;
	}
	
	
	

}
